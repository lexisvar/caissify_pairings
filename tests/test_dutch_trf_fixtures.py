def parse_trf(filepath: Path) -> dict:
    """
    Parse a TRF16 file into a structured dict.

    Returns:
        {
            "total_rounds": int,
            "num_players": int,
            "players": [
                {
                    "starting_number": int,
                    "name": str,
                    "rating": int,
                    "title_rating": int,
                    "rounds": [
                        {
                            "opponent_sn": int,  # 0 = no opponent (bye/absent)
                            "color": str,        # "w", "b", or "-"
                            "result": str,       # "1","0","=","H","U","Z","+","-","F"
                        },
                        ...
                    ],
                },
                ...
            ],
        }
    """
    text = filepath.read_text()
    lines = text.strip().split("\n")

    total_rounds = 0
    num_players = 0
    players = []

    for line in lines:
        if line.startswith("142"):
            total_rounds = int(line[4:].strip())
        elif line.startswith("062"):
            num_players = int(line[4:].strip())
        elif line.startswith("001"):
            player = _parse_player_line(line, total_rounds)
            if player:
                players.append(player)

    return {
        "total_rounds": total_rounds,
        "num_players": num_players,
        "players": players,
    }


def _parse_player_line(line: str, total_rounds: int) -> Optional[dict]:
    """
    Parse a single 001 player line from TRF16.

    Fixed-width format:
      Col 0-2:   "001"
      Col 4-7:   starting number (right-justified)
      Col 9:     sex
      Col 14-46: name (left-justified)
      Col 48-51: FIDE rating
      Col 53-55: federation
      Col 69-78: birth date
      Col 80-83: points
      Col 85-88: rank
      Col 91+:   round data (10 chars each)
    """
    if len(line) < 80:
        return None

    try:
        sn = int(line[4:8].strip())
    except (ValueError, IndexError):
        return None

    name = line[14:47].strip()

    try:
        rating = int(line[48:52].strip())
    except (ValueError, IndexError):
        rating = 0

    # Title rating (optional, col 53-56 area varies)
    # In these files, it appears after the FIDE rating
    try:
        title_rating = int(line[53:61].strip())
    except (ValueError, IndexError):
        title_rating = rating

    try:
        score = float(line[80:84].strip())
    except (ValueError, IndexError):
        score = 0.0

    # Parse round results starting at col 91
    rounds = []
    round_data = line[91:]
    for r in range(total_rounds):
        start = r * 10
        end = start + 10
        chunk = round_data[start:end] if start < len(round_data) else ""
        if len(chunk.strip()) == 0:
            rounds.append({"opponent_sn": 0, "color": "-", "result": "-"})
            continue

        # Format: "NNNN C R  " (4-char opponent, space, color, space, result, 2 spaces)
        try:
            opp_str = chunk[0:4].strip()
            opp_sn = int(opp_str) if opp_str and opp_str != "0000" else 0
        except (ValueError, IndexError):
            opp_sn = 0

        try:
            color = chunk[5].strip() if len(chunk) > 5 else "-"
        except IndexError:
            color = "-"

        try:
            result = chunk[7].strip() if len(chunk) > 7 else "-"
        except IndexError:
            result = "-"

        rounds.append({
            "opponent_sn": opp_sn,
            "color": color,
            "result": result,
        })

    return {
        "starting_number": sn,
        "name": name,
        "rating": rating,
        "title_rating": title_rating,
        "score": score,
        "rounds": rounds,
    }


def _result_to_points(result: str) -> float:
    """Convert TRF result code to points."""
    if result in ("1", "+"):
        return 1.0
    elif result in ("=",):
        return 0.5
    elif result in ("H", "P"):  # half-point bye, PAB
        return 0.5
    elif result in ("X",):
        return 0.5
    elif result in ("0", "-", "Z", "F"):
        return 0.0
    elif result in ("U", "W"):  # full-point bye
        return 1.0
    return 0.0


def _is_played_game(result: str) -> bool:
    """Check if this result represents an actual game (not a bye/forfeit/absent)."""
    return result in ("1", "0", "=")


def _is_bye(result: str) -> bool:
    """Check if this result is any kind of bye."""
    return result in ("H", "U", "Z", "F", "W", "P")


def _is_pairing_bye(result: str) -> bool:
    """Check if this result is a pairing-allocated bye (not voluntary)."""
    return result in ("U", "W")


def get_unavailable_players(trf_data: dict, round_num: int) -> Set[int]:
    """
    Get players who are unavailable for pairing in the given round.

    Players with voluntary byes (H=half-point, Z=zero-point) or who were
    absent/forfeited are not available for engine pairing.
    """
    unavailable = set()
    rnd_idx = round_num - 1
    for p in trf_data["players"]:
        sn = p["starting_number"]
        if rnd_idx >= len(p["rounds"]):
            continue
        rd = p["rounds"][rnd_idx]
        result = rd["result"]
        # Voluntary byes and zero-point byes mean the player was not available
        if result in ("H", "Z", "F"):
            unavailable.add(sn)
        # Forfeit loss with no opponent also means absent
        if result == "-" and rd["opponent_sn"] == 0:
            unavailable.add(sn)
    return unavailable


def build_round_state(trf_data: dict, up_to_round: int) -> Tuple[
    List[dict], Set[Tuple[int, int]], int
]:
    """
    Build the engine input state for pairing round `up_to_round + 1`.

    Replays rounds 1..up_to_round to accumulate:
    - Player scores and color history
    - Previous pairings set

    Returns:
        (players_list, previous_pairings, next_round_number)
    """
    players_by_sn = {}
    for p in trf_data["players"]:
        players_by_sn[p["starting_number"]] = {
            "id": p["starting_number"],
            "name": p["name"],
            "score": 0.0,
            "rating": p["rating"],
            "starting_number": p["starting_number"],
            "color_hist": [],
            "float_history": [],
            "bye_count": 0,
        }

    previous_pairings: Set[Tuple[int, int]] = set()

    for rnd_idx in range(up_to_round):
        for p in trf_data["players"]:
            sn = p["starting_number"]
            if rnd_idx >= len(p["rounds"]):
                continue
            rd = p["rounds"][rnd_idx]
            result = rd["result"]
            color = rd["color"]
            opp_sn = rd["opponent_sn"]

            # Accumulate score
            players_by_sn[sn]["score"] += _result_to_points(result)

            # Track color history (only for actual games and forfeit wins/losses)
            if color in ("w", "b"):
                if result not in ("H", "Z", "U", "W", "F", "P"):
                    players_by_sn[sn]["color_hist"].append(
                        "white" if color == "w" else "black"
                    )
                elif result in ("+", "-"):
                    # Forfeits still assign colors
                    players_by_sn[sn]["color_hist"].append(
                        "white" if color == "w" else "black"
                    )

            # Track byes
            if _is_bye(result):
                players_by_sn[sn]["bye_count"] += 1

            # Track previous pairings (actual games + forfeits)
            if opp_sn > 0 and result not in ("H", "Z", "U", "W", "F", "P"):
                pair = tuple(sorted([sn, opp_sn]))
                previous_pairings.add(pair)

    players = list(players_by_sn.values())
    return players, previous_pairings, up_to_round + 1


def extract_round_pairings(trf_data: dict, round_num: int) -> Set[Tuple[int, int]]:
    """
    Extract the known pairings for a specific round from the TRF data.

    Returns set of (min_sn, max_sn) for actual games,
    and (player_sn, 0) for byes.
    """
    pairings = set()
    seen = set()

    for p in trf_data["players"]:
        sn = p["starting_number"]
        if sn in seen:
            continue

        rnd_idx = round_num - 1
        if rnd_idx >= len(p["rounds"]):
            continue

        rd = p["rounds"][rnd_idx]
        opp_sn = rd["opponent_sn"]
        result = rd["result"]

        if opp_sn > 0 and result not in ("H", "Z", "U", "W", "F", "P"):
            pair = tuple(sorted([sn, opp_sn]))
            pairings.add(pair)
            seen.add(sn)
            seen.add(opp_sn)
        elif _is_pairing_bye(result):
            pairings.add((sn, 0))
            seen.add(sn)

    return pairings


def our_pairings_to_set(pairings: List[dict]) -> Set[Tuple[int, int]]:
    """Convert our engine output to a normalized set for comparison."""
    result = set()
    for p in pairings:
        if p.get("bye"):
            result.add((p["white_id"], 0))
        else:
            pair = tuple(sorted([p["white_id"], p["black_id"]]))
            result.add(pair)
    return result


# ---------------------------------------------------------------------------
# Structural validation (does not require matching reference pairings)
# ---------------------------------------------------------------------------

def validate_pairings_structure(
    pairings: List[dict],
    players: List[dict],
    previous_pairings: Set[Tuple[int, int]],
    round_num: int,
    total_rounds: int,
) -> List[str]:
    """
    Validate FIDE structural constraints on pairings.

    Returns list of violation strings (empty = all good).
    """
    violations = []
    regular = [p for p in pairings if not p.get("bye")]
    byes = [p for p in pairings if p.get("bye")]
    pmap = {p["id"]: p for p in players}

    # 1. No repeat pairings
    for p in regular:
        pair = tuple(sorted([p["white_id"], p["black_id"]]))
        if pair in previous_pairings:
            violations.append(f"Repeat pairing: {pair}")

    # 2. Each player appears at most once
    ids_seen = []
    for p in pairings:
        ids_seen.append(p["white_id"])
        if p.get("black_id") is not None:
            ids_seen.append(p["black_id"])
    if len(ids_seen) != len(set(ids_seen)):
        violations.append("Duplicate player in pairings")

    # 3. Table numbers are sequential
    tables = [p["table"] for p in pairings]
    if tables != list(range(1, len(tables) + 1)):
        violations.append(f"Non-sequential tables: {tables}")

    # 4. Color constraints (not last round)
    is_last = round_num >= total_rounds
    if not is_last:
        for p in regular:
            for pid, color in [
                (p["white_id"], "white"),
                (p["black_id"], "black"),
            ]:
                player = pmap.get(pid, {})
                hist = list(player.get("color_hist", []))
                test_hist = hist + [color]
                # No 3-in-a-row
                if len(test_hist) >= 3 and (
                    test_hist[-1] == test_hist[-2] == test_hist[-3]
                ):
                    violations.append(
                        f"3 same colours for player {pid}: {test_hist}"
                    )
                # Color diff ≤ ±2
                new_diff = test_hist.count("white") - test_hist.count("black")
                if abs(new_diff) > 2:
                    violations.append(
                        f"Colour diff {new_diff} for player {pid}"
                    )

    return violations


