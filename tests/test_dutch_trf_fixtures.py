class TestTRFParser(unittest.TestCase):
    """Verify the TRF parser works correctly on fixture files."""

    def test_parser_reads_all_fixtures(self):
        """Every fixture should parse without errors."""
        for fp in _get_fixtures():
            with self.subTest(file=fp.name):
                data = parse_trf(fp)
                self.assertGreater(data["total_rounds"], 0, f"{fp.name}: no rounds")
                self.assertGreater(len(data["players"]), 0, f"{fp.name}: no players")

    def test_parser_player_count_matches(self):
        """The number of parsed 001 lines should match the header."""
        for fp in _get_fixtures():
            with self.subTest(file=fp.name):
                data = parse_trf(fp)
                self.assertEqual(
                    len(data["players"]),
                    data["num_players"],
                    f"{fp.name}: player count mismatch",
                )

    def test_parser_round_count(self):
        """Each player should have total_rounds entries."""
        for fp in _get_fixtures():
            with self.subTest(file=fp.name):
                data = parse_trf(fp)
                for p in data["players"]:
                    self.assertEqual(
                        len(p["rounds"]),
                        data["total_rounds"],
                        f"{fp.name}: player {p['starting_number']} "
                        f"has {len(p['rounds'])} rounds, expected {data['total_rounds']}",
                    )


class TestRound1Deterministic(unittest.TestCase):
    """
    Round 1 is fully deterministic in Dutch: top-half vs bottom-half.
    Our engine's round 1 should always match the reference TRF.
    """

    def test_round1_matches_all_fixtures(self):
        """Round 1 pairings should match the TRF for every fixture."""
        for fp in _get_fixtures():
            with self.subTest(file=fp.name):
                data = parse_trf(fp)

                # Build initial state (no prior rounds)
                players, prev, rnd = build_round_state(data, 0)

                # Filter out players unavailable in round 1
                # (voluntary byes, zero-point byes, absences)
                unavailable = get_unavailable_players(data, 1)
                active_players = [
                    p for p in players if p["id"] not in unavailable
                ]

                expected = extract_round_pairings(data, 1)

                if not expected:
                    continue  # Skip if no extractable pairings

                our = dutch_pairings(
                    active_players, prev, rnd, data["total_rounds"]
                )
                our_set = our_pairings_to_set(our)

                # Compare actual game pairings only (not bye assignment details)
                our_games = {p for p in our_set if p[1] != 0}
                expected_games = {p for p in expected if p[1] != 0}

                self.assertEqual(
                    our_games,
                    expected_games,
                    f"{fp.name} R1: ours={sorted(our_games)} expected={sorted(expected_games)}",
                )


class TestStructuralValidation(unittest.TestCase):
    """
    For every round of every fixture, validate that our engine's pairings
    satisfy all FIDE structural constraints (no repeats, colour rules, etc.),
    even if they don't exactly match the reference pairings.
    """

    def test_no_violations_all_fixtures(self):
        """Our engine should produce structurally valid pairings for all rounds."""
        for fp in _get_fixtures():
            with self.subTest(file=fp.name):
                data = parse_trf(fp)
                total_rounds = data["total_rounds"]

                # Only test the first few rounds to avoid slow tests on 31+ round files
                rounds_to_test = min(total_rounds, 9)

                for rnd in range(1, rounds_to_test + 1):
                    players, prev, next_rnd = build_round_state(data, rnd - 1)

                    # Filter out players who have a bye/absent result for all
                    # remaining rounds (withdrawn players)
                    active_players = []
                    for p in players:
                        trf_p = next(
                            (tp for tp in data["players"]
                             if tp["starting_number"] == p["id"]),
                            None,
                        )
                        if trf_p and rnd - 1 < len(trf_p["rounds"]):
                            active_players.append(p)

                    if len(active_players) < 2:
                        continue

                    our = dutch_pairings(
                        active_players, prev, next_rnd, total_rounds,
                    )

                    violations = validate_pairings_structure(
                        our, active_players, prev, next_rnd, total_rounds,
                    )

                    self.assertEqual(
                        len(violations), 0,
                        f"{fp.name} R{rnd}: {violations}",
                    )


class TestSmallTournaments(unittest.TestCase):
    """Focused tests on small (7-player) tournaments — rich in bye edge cases."""

    def _run_small_tournament(self, filename: str):
        fp = FIXTURES_DIR / filename
        if not fp.exists():
            self.skipTest(f"{filename} not found")

        data = parse_trf(fp)
        total_rounds = data["total_rounds"]

        all_match = True
        mismatches = []

        for rnd in range(1, total_rounds + 1):
            players, prev, next_rnd = build_round_state(data, rnd - 1)
            expected = extract_round_pairings(data, rnd)

            if not expected:
                continue

            our = dutch_pairings(players, prev, next_rnd, total_rounds)
            our_set = our_pairings_to_set(our)

            # Compare game pairings (ignore bye player identity for now)
            our_games = {p for p in our_set if p[1] != 0}
            expected_games = {p for p in expected if p[1] != 0}

            if our_games != expected_games:
                all_match = False
                mismatches.append(
                    f"R{rnd}: ours={sorted(our_games)} ref={sorted(expected_games)}"
                )

            # Always validate structure
            violations = validate_pairings_structure(
                our, players, prev, next_rnd, total_rounds,
            )
            self.assertEqual(
                len(violations), 0,
                f"{filename} R{rnd}: {violations}",
            )

        # Report mismatches (informational, not failure — our engine may
        # make different but equally valid choices in later rounds)
        if mismatches:
            print(f"\n  {filename} mismatches ({len(mismatches)}/{total_rounds}):")
            for m in mismatches:
                print(f"    {m}")

    def test_small_clean(self):
        self._run_small_tournament("small_7p5r_clean.trf")

    def test_small_byes(self):
        self._run_small_tournament("small_7p5r_byes.trf")

    def test_small_varied(self):
        self._run_small_tournament("small_7p5r_varied.trf")


