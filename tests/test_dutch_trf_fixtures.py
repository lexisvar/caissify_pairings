"""
TRF fixture-based validation tests for the FIDE Dutch System Pairing Engine.

Reads pre-generated tournament TRF files (from tests/fixtures/) and validates
our Dutch engine by replaying each round:

1. Parse the TRF to extract players, round-by-round results, and pairings.
2. For each round, feed the engine the tournament state *before* that round.
3. Compare the engine's output against the known-correct pairings from the TRF.

This gives us ground-truth validation against thousands of tournament
configurations generated by a reference engine (JaVaFo/bbpPairings).

Fixtures cover:
- Small tournaments (7 players, 5 rounds) — bye edge cases
- Medium tournaments (9p/7r, 15p/9r) — forfeits, half-point byes, zero-point byes
- Large tournaments (30p/11r, 50p/15r, 50p/31r) — stress & many-byes/forfeits
- Misc edge cases from reference collections
"""

import os
import re
import unittest
from pathlib import Path
from typing import Dict, List, Optional, Set, Tuple

from caissify_pairings.engines.dutch import dutch_pairings

FIXTURES_DIR = Path(__file__).parent / "fixtures"


